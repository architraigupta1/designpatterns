package com.archit.designpatterns.facade;

public class PopcornPopper {
  public void on() {
    System.out.println("Popcorn popper turned on");
  }

  public void off() {
    System.out.println("Popcorn popper turned off");
  }

  public void pop() {
    System.out.println("Popping popcorns");
  }
}
